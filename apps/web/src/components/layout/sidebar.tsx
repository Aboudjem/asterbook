export function Sidebar({ activePath = '', collapsed = false, onToggle }: SidebarProps) {
  const [expandedSections, setExpandedSections] = useState<string[]>(['Main', 'Gaming', 'Finance']);

  const toggleSection = (title: string) => {
    setExpandedSections((prev) =>
      prev.includes(title) ? prev.filter((t) => t !== title) : [...prev, title]
    );
  };

  return (
    <>
      {!collapsed && (
        <div className="lg:hidden fixed inset-0 bg-black/50 z-40" onClick={onToggle} />
      )}

      <aside
        className={`pc-sidebar fixed top-16 left-0 h-[calc(100vh-4rem)] z-40 transition-transform duration-300 ease-in-out overflow-y-auto ${
          collapsed ? '-translate-x-full lg:translate-x-0' : 'translate-x-0'
        }`}
        style={{
          width: collapsed ? '80px' : '260px',
          background: 'rgba(2, 6, 23, 0.65)',
          backdropFilter: 'blur(16px)',
          WebkitBackdropFilter: 'blur(16px)',
          borderRight: '1px solid rgba(255, 255, 255, 0.08)',
        }}
      >
        <button
          onClick={onToggle}
          className="absolute -right-3 top-6 w-6 h-6 rounded-full bg-slate-800 border border-slate-600 flex items-center justify-center text-slate-400 hover:text-white hover:bg-slate-700 transition-colors hidden lg:flex"
        >
          <svg className={`w-4 h-4 transition-transform ${collapsed ? 'rotate-180' : ''}`} fill="none" viewBox="0 0 24 24" stroke="currentColor">
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M15 19l-7-7 7-7" />
          </svg>
        </button>

        <nav className="p-4 pb-32">
          {navSections.map((section) => (
            <div key={section.title} className="mb-4">
              {!collapsed && (
                <button
                  onClick={() => toggleSection(section.title)}
                  className="w-full flex items-center justify-between px-2 py-1 text-xs font-semibold text-slate-500 uppercase tracking-wider hover:text-slate-400"
                >
                  <span>{section.title}</span>
                  <svg
                    className={`w-3 h-3 transition-transform ${expandedSections.includes(section.title) ? 'rotate-180' : ''}`}
                    fill="none"
                    viewBox="0 0 24 24"
                    stroke="currentColor"
                  >
                    <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M19 9l-7 7-7-7" />
                  </svg>
                </button>
              )}

              {(collapsed || expandedSections.includes(section.title)) && (
                <div className="space-y-1 mt-2">
                  {section.items.map((item) => {
                    const isActive = activePath === item.href || activePath.startsWith(item.href + '/');
                    return (
                      <Link
                        key={item.href}
                        href={item.href}
                        className={`pc-item flex items-center gap-3 px-4 py-2.5 rounded-lg transition-all duration-200 group ${
                          isActive ? 'active' : 'text-slate-400 hover:text-cyan-400 hover:bg-white/5'
                        }`}
                        style={isActive ? { background: 'rgba(0, 243, 255, 0.1)', color: '#00f3ff' } : {}}
                        title={collapsed ? item.label : undefined}
                      >
                        <span className={`flex-shrink-0 ${isActive ? 'text-cyan-400' : 'text-slate-400 group-hover:text-cyan-400'} transition-colors`}>
                          {item.icon}
                        </span>
                        {!collapsed && <span className="font-medium text-sm whitespace-nowrap">{item.label}</span>}
                      </Link>
                    );
                  })}
                </div>
              )}
            </div>
          ))}
        </nav>

        {!collapsed && (
          <div className="absolute bottom-0 left-0 right-0 p-4 border-t border-white/5 bg-[rgba(2,6,23,0.9)]">
            <Link
              href="/premium-analysis"
              className="block p-4 rounded-xl"
              style={{
                background: 'linear-gradient(135deg, rgba(0, 243, 255, 0.1) 0%, rgba(59, 130, 246, 0.1) 100%)',
                border: '1px solid rgba(0, 243, 255, 0.2)',
              }}
            >
              <div className="flex items-center gap-2 mb-2">
                <svg className="w-4 h-4 text-cyan-400" fill="none" viewBox="0 0 24 24" stroke="currentColor">
                  <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M13 10V3L4 14h7v7l9-11h-7z" />
                </svg>
                <span className="text-xs font-semibold text-cyan-400">PRO</span>
              </div>
              <p className="text-xs text-slate-400 mb-3">Unlock premium features and exclusive content.</p>
              <span className="block w-full py-2 text-xs font-semibold text-center text-white rounded-lg" style={{ background: 'linear-gradient(135deg, #00f3ff 0%, #3b82f6 100%)' }}>
                Upgrade Now
              </span>
            </Link>
          </div>
        )}
      </aside>
    </>
  );
}

export default Sidebar;
